#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - functions
 * @a: one
 * @b: two
 * Return : Always 0
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - functions
 * @a: one
 * @b: two
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - functions
 * @a: one
 * @b: two
 * Return: Always 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - functions
 * @a: one
 * @b: two
 * Return: Always 0
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - functions
 * @a: one
 * @b: two
 * Return: Always 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
